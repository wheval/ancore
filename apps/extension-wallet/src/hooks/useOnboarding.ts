/**
 * Encrypted secret key payload from crypto package
 */
interface EncryptedSecretKeyPayload {
  version: number;
  iterations: number;
  salt: string;
  iv: string;
  ciphertext: string;
}

/**
 * Password validation result from crypto package
 */
type PasswordValidationResult = {
  valid: boolean;
  strength: 'weak' | 'medium' | 'strong';
  reasons: string[];
};

/**
 * Onboarding step enum
 */
export type OnboardingStep = 'welcome' | 'generate' | 'verify' | 'password' | 'deploy' | 'success';

/**
 * Wallet account data after onboarding
 */
export interface OnboardedAccount {
  publicKey: string;
  contractId: string;
  encryptedMnemonic: EncryptedSecretKeyPayload;
}

/**
 * Onboarding state
 */
export interface OnboardingState {
  step: OnboardingStep;
  mnemonic: string | null;
  verified: boolean;
  password: string | null;
  account: OnboardedAccount | null;
  error: string | null;
  isLoading: boolean;
}

/**
 * Password strength result
 */
export type PasswordStrength = {
  isValid: boolean;
  score: number;
  feedback: string[];
};

/**
 * Convert crypto package result to our format
 */
function toPasswordStrength(result: PasswordValidationResult): PasswordStrength {
  const score = result.strength === 'strong' ? 4 : result.strength === 'medium' ? 2 : 0;
  return {
    isValid: result.valid,
    score,
    feedback: result.reasons,
  };
}

