/**
 * Derive a keypair from mnemonic
 */
function deriveKeypair(mnemonic: string): Keypair {
  // In production, use bip39 to derive seed, then derive ed25519 keypair
  // For this implementation, we'll use a deterministic derivation
  const seed = mnemonic.split(' ').reduce((acc, word, i) => {
    return acc + word.charCodeAt(0) * (i + 1);
  }, 0);

  const secretArray = new Uint8Array(32);
  const seedStr = String(seed);
  for (let i = 0; i < 32; i++) {
    secretArray[i] = seedStr.charCodeAt(i % seedStr.length) ^ (seed + i);
  }

  // Convert to Buffer for Stellar SDK
  const buffer = Buffer.from(secretArray);
  return Keypair.fromRawEd25519Seed(buffer);
}

/**
 * Onboarding hook for managing the complete onboarding flow
 */
export function useOnboarding() {
  const [state, setState] = useState<OnboardingState>(DEFAULT_STATE);

  /**
   * Move to the next step
   */
  const goToNextStep = useCallback(() => {
    setState((prev: OnboardingState) => {
      const steps: OnboardingStep[] = [
        'welcome',
        'generate',
        'verify',
        'password',
        'deploy',
        'success',
      ];
      const currentIndex = steps.indexOf(prev.step);
      if (currentIndex < steps.length - 1) {
        return { ...prev, step: steps[currentIndex + 1], error: null };
      }
      return prev;
    });
  }, []);

  /**
   * Go back to the previous step
   */
  const goToPreviousStep = useCallback(() => {
    setState((prev: OnboardingState) => {
      const steps: OnboardingStep[] = [
        'welcome',
        'generate',
        'verify',
        'password',
        'deploy',
        'success',
      ];
      const currentIndex = steps.indexOf(prev.step);
      if (currentIndex > 0) {
        return { ...prev, step: steps[currentIndex - 1], error: null };
      }
      return prev;
    });
  }, []);

  /**
   * Go to a specific step
   */
  const goToStep = useCallback((step: OnboardingStep) => {
    setState((prev: OnboardingState) => ({ ...prev, step, error: null }));
  }, []);

  /**
   * Start the onboarding process
   */
  const startOnboarding = useCallback(() => {
    setState((prev: OnboardingState) => ({ ...prev, step: 'welcome' }));
  }, []);

  /**
   * Generate a new mnemonic
   */
  const generateMnemonicHandler = useCallback(() => {
    const mnemonic = generateMnemonic();
    setState((prev: OnboardingState) => ({ ...prev, mnemonic, step: 'generate' }));
  }, []);

  /**
   * Verify the mnemonic (mark as verified)
   */
  const verifyMnemonicHandler = useCallback(() => {
    setState((prev: OnboardingState) => ({ ...prev, verified: true }));
  }, []);

  /**
   * Set the password
   */
  const setPasswordHandler = useCallback((password: string) => {
    setState((prev: OnboardingState) => ({ ...prev, password }));
  }, []);

  /**
   * Check password strength
   */
  const checkPasswordStrength = useCallback((password: string): PasswordStrength => {
    const result = validatePasswordStrength(password);
    return toPasswordStrength(result as unknown as PasswordValidationResult);
  }, []);

  /**
   * Encrypt and store the mnemonic
   */
  const encryptMnemonic = useCallback(
    async (password: string): Promise<EncryptedSecretKeyPayload | null> => {
      if (!state.mnemonic) return null;

      try {
        const encrypted = await encryptSecretKey(state.mnemonic, password);
        return encrypted;
      } catch (error) {
        setState((prev: OnboardingState) => ({
          ...prev,
          error: error instanceof Error ? error.message : 'Failed to encrypt mnemonic',
        }));
        return null;
      }
    },
    [state.mnemonic]
  );

  /**
   * Deploy the account contract to the network
   */
  const deployAccount = useCallback(
    async (
      network: Network = 'testnet'
    ): Promise<{ publicKey: string; contractId: string } | null> => {
      if (!state.mnemonic) {
        setState((prev: OnboardingState) => ({ ...prev, error: 'No mnemonic generated' }));
        return null;
      }

      setState((prev: OnboardingState) => ({ ...prev, isLoading: true, error: null }));

      try {
        const keypair = deriveKeypair(state.mnemonic);
        const publicKey = keypair.publicKey();

        // Initialize Stellar client
        const client = new StellarClient({ network });

        // Fund account with Friendbot on testnet
        if (network === 'testnet') {
          await client.fundWithFriendbot(publicKey);
        }

        // Wait for account to be created
        await new Promise((resolve) => setTimeout(resolve, 3000));

        // Create a simple account initialization
        // In production, this would deploy the actual account contract
        const contractId = `CAS${publicKey.slice(1, 56)}`;

        const account: OnboardedAccount = {
          publicKey,
          contractId,
          encryptedMnemonic: {
            version: 1,
            iterations: 100000,
            ciphertext: publicKey, // In production, use actual encrypted mnemonic
            iv: 'test-iv',
            salt: 'test-salt',
          },
        };

        // Save to storage
        localStorage.setItem(WALLET_STATE_KEY, 'locked');
        localStorage.setItem(
          ACCOUNTS_KEY,
          JSON.stringify([
            {
              id: '1',
              publicKey: account.publicKey,
              contractId: account.contractId,
              createdAt: Date.now(),
            },
          ])
        );

        setState((prev: OnboardingState) => ({
          ...prev,
          account,
          step: 'success',
          isLoading: false,
        }));

        return { publicKey, contractId };
      } catch (error) {
        const errorMessage = error instanceof Error ? error.message : 'Failed to deploy account';
        setState((prev: OnboardingState) => ({
          ...prev,
          error: errorMessage,
          isLoading: false,
        }));
        return null;
      }
    },
    [state.mnemonic]
  );

  /**
   * Reset the onboarding state
   */
  const reset = useCallback(() => {
    setState(DEFAULT_STATE);
  }, []);

  /**
   * Clear any error
   */
  const clearError = useCallback(() => {
    setState((prev: OnboardingState) => ({ ...prev, error: null }));
  }, []);

  return {
    // State
    step: state.step,
    mnemonic: state.mnemonic,
    verified: state.verified,
    password: state.password,
    account: state.account,
    error: state.error,
    isLoading: state.isLoading,

    // Actions
    startOnboarding,
    goToNextStep,
    goToPreviousStep,
    goToStep,
    generateMnemonic: generateMnemonicHandler,
    verifyMnemonic: verifyMnemonicHandler,
    setPassword: setPasswordHandler,
    checkPasswordStrength,
    encryptMnemonic,
    deployAccount,
    reset,
    clearError,
  };
}
